
public class Naturals_Numbers {
	
	public static void main(String[] args) {
		
		
		    int n = 10; // Change this value to the desired positive integer
		    int sum = 0;
		    
		    for (int i = 1; i <= n; i++) {
		        sum =sum+i;
		    }

		    System.out.println("The sum of natural numbers up to " + n + " is: " + sum);
		

		
		
		
		
		
		
		
		
		
		
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	

}
