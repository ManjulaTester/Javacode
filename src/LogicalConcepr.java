
public class LogicalConcepr {
	
	
	public static void main(String[] args) {
		               
		int num = 5;
		
		if (num>0 && num<10) {                    // AND operator both the condition true output is 5
			
			System.out.println(num);
			
		}
		
		if (num<0 || num >10) {                    // OR operator both the condition is false no output is executed
			
			System.out.println(num);
			
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}
	
	
	
	
	
	
	
	
	
	
	
	

}
